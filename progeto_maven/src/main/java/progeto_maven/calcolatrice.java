package progeto_maven;

public class calcolatrice {

	public calcolatrice() {
		
		
	}

	public static double somma(double n1, double n2)
	
	{
		double res=0;
		
		res=n1+n2;
		
		return res;
			
	}
	
	
public static double moltiplicazione(double n1, double n2)
	
	{
		double res=0;
		
		res=n1*n2;
		
		return res;
			
	}
}
